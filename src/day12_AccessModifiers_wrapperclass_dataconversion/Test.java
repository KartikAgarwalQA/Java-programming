package day12_AccessModifiers_wrapperclass_dataconversion;
public class Test 
{
	
	//default
	int x=100;
	void m1()
	{
		System.out.println("This is a m1 method");
	}
	
	//private
	private int y=200;
	private void m2()
	{
		System.out.println("This is a m2 method");
	}
	
	//protected 
	protected int z=200;
	protected void m3()
	{
		System.out.println("This is a m3 method");
	}
	//public 
	
	public int a=20023;
	public void m4()
	{
		System.out.println("This is a m4 method");
	}
	
	
}
