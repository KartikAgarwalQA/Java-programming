package day1_Variables_DataTypes;

public class Datatypes {

	public static void main(String[] args) {
		
		// --Primitive Data types
		int a=200,b=300;
		System.out.println("Value of a:"+a);
		System.out.println("Value of b:"+b);
		
		long x =123456789;
		System.out.println("Value of x:"+x);
		
		byte c=-100;
		System.out.println("Value of c:"+c);
		
		short s=3600;
		System.out.println("Value of s:"+s);
		
		//Decimal numbers - Float , double
		float itemprice=15.123456F;
		System.out.println("Value of item:"+itemprice);
		
		double price=17.123456789123456789;
		System.out.println("Value of item2:"+price);
		
		double du=125;
		System.out.println("Value of du:"+du);
		
		
		char grade='A';
		System.out.println("Grade:"+grade);
		
		boolean TF=true;
		System.out.println("Value of TF boolean:"+TF);
		
		//-- Non-primitive Data types
		String name="Kartik";
		System.out.println("Name:"+name);
		
		String Demo1="A";
		char Demo2='C';
		System.out.println(Demo1);
		System.out.println(Demo2);
		
		String demo3="123"; //Valid
		System.out.println(demo3);

	}

}
